#!/bin/bash

# Остановка текущего процесса бота
echo "Остановка текущего процесса бота..."
pkill -f "python3 main.py" || echo "Процесс не найден"

# Очистка временной директории
echo "Очистка временной директории..."
rm -f temp/*

# Запуск бота
echo "Запуск бота..."
python3 main.py &

echo "Бот перезапущен!"